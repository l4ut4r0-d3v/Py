# iterar es recorrer cada elemento de un conjunto de datos
cadena = 'hola'

# el ciclo for itera o recorre, 
# despues se le da una variable que se le da cada uno de los areglos, 
# despues 'int' dentro (de cada elemento), para saber que cadenas hay que iterar
for letra in cadena:
    print(letra)
# Se puede usar opcionalmente el 'else'
else:
    print('Fin del ciclo for')